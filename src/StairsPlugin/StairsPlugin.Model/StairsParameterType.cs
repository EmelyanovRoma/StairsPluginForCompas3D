namespace StairsPlugin.Model
{
    /// <summary>
    /// Описывает параметры лестницы.
    /// </summary>
    public enum StairsParameterType
    {
        /// <summary>
        /// Высота лестницы.
        /// </summary>
        Height,

        /// <summary>
        /// Ширина лестницы.
        /// </summary>
        Width,

        /// <summary>
        /// Толщина лестницы.
        /// </summary>
        Thickness,

        /// <summary>
        /// Ширина балки.
        /// </summary>
        StringerWidth,

        /// <summary>
        /// Длина ступени.
        /// </summary>
        StepLength,

        /// <summary>
        /// Высота ступени.
        /// </summary>
        StepHeight,

        /// <summary>
        /// Расстояние между ступенями.
        /// </summary>
        StepGap
    }
}
